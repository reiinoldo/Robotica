
public class No {
	private No frente;
	private No direita;
	private No esquerda;
	
	public No getFrente() {
		return frente;
	}
	public void setFrente(No frente) {
		this.frente = frente;
	}
	public No getDireita() {
		return direita;
	}
	public void setDireita(No direita) {
		this.direita = direita;
	}
	public No getEsquerda() {
		return esquerda;
	}
	public void setEsquerda(No esquerda) {
		this.esquerda = esquerda;
	}
}
